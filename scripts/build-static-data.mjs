import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const dataDir = path.join(root, 'src', 'data');
const outFile = path.join(root, 'static', 'js', 'data.js');

const readJson = (relativePath, fallback = null) => {
  const filePath = path.join(dataDir, relativePath);
  if (!fs.existsSync(filePath)) return fallback;
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
};

const readJsonFolder = (relativePath) => {
  const folder = path.join(dataDir, relativePath);
  if (!fs.existsSync(folder)) return {};

  return Object.fromEntries(
    fs.readdirSync(folder)
      .filter((file) => file.endsWith('.json'))
      .sort()
      .map((file) => [path.basename(file, '.json'), readJson(path.join(relativePath, file))]),
  );
};

